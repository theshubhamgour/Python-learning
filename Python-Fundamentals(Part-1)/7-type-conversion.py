#Type Conversion in Python
# Type conversion is the process of converting a value from one data type to another. Python provides
# several built-in functions to perform type conversion.

# Converting to Integer
x = 10.5
y = int(x)  # Converts float to integer
print("Value of y (int):", y)

# Converting to Float
a = 5
b = float(a)  # Converts integer to float
print("Value of b (float):", b)

# Converting to String
num = 100
text = str(num)  # Converts integer to string
print("Value of text (str):", text)

# Converting to Boolean
value = 0
bool_value = bool(value)  # Converts integer to boolean
print("Value of bool_value (bool):", bool_value)